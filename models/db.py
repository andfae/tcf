# host names must be a list of allowed host names (glob syntax allowed)
auth = Auth(db, host_names=configuration.get('host.names'))

# -------------------------------------------------------------------------
# create all tables needed by auth, maybe add a list of extra fields
# -------------------------------------------------------------------------

# personalizzazione: extra field widget color su auth_user
# auth.settings.extra_fields['auth_user']= [
#   Field('colore', 'string', default='#ff8d1c', widget=lambda f,v: SQLFORM.widgets.string.widget(f,v, _value=v, _type='color',_data_text='hidden', _hex='true'))]

# --------- personalizzazioni per gestire lo username piuttosto che l'email e per gestire i log ---------
auth.define_tables(username=True, signature=True)

# tolgo i messaggi di logged in e logged out
auth.messages.logged_in = None
auth.messages.logged_out = None

# in model db.py after auth.define_tables
# questo passaggio serve per definire la lunghezza minima della password per gli utenti
default_password_validators = [def_val for def_val in db.auth_user.password.requires]
added_password_validators = [IS_LENGTH(minsize=8,error_message=T('Password non valida'))]
password_validators = added_password_validators + default_password_validators
db.auth_user.password.requires = password_validators

# disabilitazione funzioni utenti
auth.settings.actions_disabled.append('register')
# auth.settings.actions_disabled = ['register']

# aumento del timeout per la sessione a 2 ore
auth.settings.expiration = 7200  # seconds

auth.settings.formstyle = 'table3cols'

# -------------------------------------------------------------------------
# configure email
# -------------------------------------------------------------------------
mail = auth.settings.mailer
mail.settings.server = 'logging' if request.is_local else configuration.get('smtp.server')
mail.settings.sender = configuration.get('smtp.sender')
mail.settings.login = configuration.get('smtp.login')
mail.settings.tls = configuration.get('smtp.tls') or False
mail.settings.ssl = configuration.get('smtp.ssl') or False

# -------------------------------------------------------------------------
# configure auth policy
# -------------------------------------------------------------------------
auth.settings.registration_requires_verification = False
auth.settings.registration_requires_approval = False
auth.settings.reset_password_requires_verification = False

# -------------------------------------------------------------------------  
# read more at http://dev.w3.org/html5/markup/meta.name.html               
# -------------------------------------------------------------------------
response.meta.author = configuration.get('app.author')
response.meta.description = configuration.get('app.description')
response.meta.keywords = configuration.get('app.keywords')
response.meta.generator = configuration.get('app.generator')

# -------------------------------------------------------------------------
# your http://google.com/analytics id                                      
# -------------------------------------------------------------------------
response.google_analytics_id = configuration.get('google.analytics_id')

